-- SPEC-AUTH-AUTHLOGS-WRITE-REPAIR-01
-- auth_logs.id must auto-generate: verify-pin inserts {staff_id, action, ip_address,
-- user_agent} omitting id, expecting a default. Originally id was bigint NOT NULL with no
-- default -> every insert failed NOT NULL and was swallowed by EdgeRuntime.waitUntil.
--
-- NOTE: prod was already repaired ad-hoc before this migration was authored (id is now an
-- IDENTITY column, sequence advanced past the 43 legacy rows). This migration is idempotent
-- so it is a no-op on the already-fixed prod DB while making the fix git-tracked and
-- reproducible on fresh environments.
--
-- Scope guard: does NOT touch verify-pin, RLS/grants on auth_logs, or the 43 existing rows.
do $$
begin
  if not exists (
    select 1 from information_schema.columns
    where table_schema='public' and table_name='auth_logs' and column_name='id' and is_identity='YES'
  ) then
    execute 'alter table public.auth_logs alter column id add generated by default as identity';
  end if;
end $$;

-- Ensure the identity sequence sits above the current max(id) so new inserts never collide
-- with pre-existing rows. greatest(...) never lowers an already-higher sequence value.
select setval(
  pg_get_serial_sequence('public.auth_logs','id'),
  greatest(
    (select coalesce(max(id),0) from public.auth_logs),
    (select last_value from pg_sequences
       where schemaname='public'
         and sequencename = split_part(pg_get_serial_sequence('public.auth_logs','id'),'.',2))
  ),
  true
);
